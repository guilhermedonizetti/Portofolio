//Curso 1

$(document).ready(function() {
	$('#curso1').click(function(){
		$('#card_curso1').toggle(300);
		$('#curso1_desc').show(300);
	})
});

$(document).ready(function() {
	$('#curso1_fechar').click(function(){
		$('#curso1_desc').toggle(300);
		$('#card_curso1').show(300);
	})
});


